# Data types in Python:
# initialising data types
int_type = 1  # integer type
float_type = 1.2  # float type
string_type = "String"  # string type
dictionary = {"Key": "Value"}  # dictionary
List = ["element1", "element2", 1, 2, 3]  # list
Boolean = True  # boolean type
Tuple = (1, 2, 3, 4, "item1", "item2")  # tuple

# printing types of variables:
print(type(int_type))
print(type(float_type))
print(type(string_type))
print(type(dictionary))
print(type(List))
print(type(Boolean))
print(type(Tuple))
